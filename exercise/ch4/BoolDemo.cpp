# include <iostream>

using namespace std;


int main()
{
	char v = 's';
	float w = 3.141;
	int x = -1;
	int y = 12345;
	int z = 0;
	cout << "Everything is true, if it is not zero." 	<< endl;
	cout << "Value of v: " << v << "\t\tBoolean x: " 	<< (bool) v << endl;
	cout << "Value of w: " << w << "\tBoolean w: " 		<< (bool) w << endl;
	cout << "Value of x: " << x << "\t\tBoolean x: " 	<< (bool) x << endl;
	cout << "Value of y: " << y << "\tBoolean y: " 		<< (bool) y << endl;
	cout << "Value of z: " << z << "\t\tBoolean z: " 	<< (bool) z << endl;
	return 0;
}
